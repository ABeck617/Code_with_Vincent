# String Formatting

print("Hello Anthony")

print("Hello {0} {1} {2}".format("Anthony", 100, False))


# Followers _ | Following _ | Likes _
followers = 100
following = 10
likes = "1M"
print("Followers {0} | Following {1} | Likes {2}".format(followers, following, likes))


# String Manipulation (Updating a String value)
myName = "Beth +"
myName += " Anthony <3"
print(myName)



x = "x"
print(x)
print(x * 10)